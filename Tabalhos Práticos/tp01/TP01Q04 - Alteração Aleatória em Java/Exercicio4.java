public class Exercicio4 {
    //Método que verifica se acabou as entradas ou não
    public static boolean isFim(String s){
        return (s.length() == 3 && s.charAt(0) == 'F' && s.charAt(1) == 'I' && s.charAt(2) == 'M');
    }

    public static String replace(String str, char c1, char c2){
        String newStr = new String();
        for (int i = 0; i < str.length(); i++) {
            if (str.charAt(i) == c1) {
                newStr += c2;
            }
            else{
                newStr += str.charAt(i);
            }
        }
        return newStr;
    }

    public static void main(String[] args) {
        Random gen = new Random();
        gen.setSeed(4);
        //System.out.println ( ( char ) ( 'a' + (Math. abs ( gen.nextInt()) % 26 ) ) ) ;

        String text = new String();
        text = MyIO.readLine();
        while (!isFim(text)) {
            char c1 = ( char ) ( 'a' + (Math. abs ( gen.nextInt()) % 26 ) );
            char c2 = ( char ) ( 'a' + (Math. abs ( gen.nextInt()) % 26 ) );
            //MyIO.println("" + c1 + c2);
            MyIO.println(replace(text, c1, c2));
            text = MyIO.readLine();
        }
    }
}

